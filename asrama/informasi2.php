            <h2>Sejarah dan Tujuan Terbentuknya Asrama IT Del</h2>
            <h4>Sejak didirikan tahun 2001, IT Del sudah mengharuskan mahasiswanya untuk tinggal diasrama tanpa
                terkecuali. Tujuannya adalah agar menunjang keberhasilan pencapaian tujuan pendidikan mahasiswanya.
                Selain itu kenapa seluruh mahasiswa diharusnya berasrama adalah agar para Bapak dan Ibu asraram dapat
                memberikan bimbingan kepada mereka selain yang diperoleh mereka dikampus,
                untuk dapat menanamkan rasa disiplin dalam diri mereka,agar mereka dapat bersosialisasi dengan semua
                teman-teman yang berasal dari berbagai tempat dan agar membiasakan diri mereka untuk dapat hidup
                mandiri.
                Biasanya asrama merupakan sekedar tempat untuk tidur, tapi tidak di IT Del. Dimana asrama juga merupakan
                tempat belajar untuk pembentukan karakter dan pendidikan rohani,
                karena IT Del mengharapkan lulusannya tidak hanya pintar dalam hal akademik tapi juga diharapkan
                memiliki karakter yang baik agar sesuai dengan moto dari Yayasan Del yakni MarTuhan,, Maroha dan
                Marbisuk.
                IT Del memiliki 8 (delapan) asrama yang dilayani oleh 10 (sepuluh) Bapak dan Ibu Asrama berasal dari
                berbagai daerah. Mereka adalah Bapak dan Ibu asrama yang dengan setia dan sabar
                menghadapi ribuan mahasiswa setiap harinya dengan berbagai karakter yang berbeda.
                Mereka menganggap merupakan suatu kehormatan dapat melayani ribuan mahasiswa IT Del. Dimana setiap
                harinya mereka memulai melakukan aktivitas bersama-sama. Mulai dari bangun pagi pukul 05.00 wib kemudian
                diawali dengan doa bersama untuk mengucap syukur dengan hari yang baru.
                Setelah itu melakukan kegiatan kebersihan mulai dari tempat tidur, lemari pakaian dan lain-lain. Tidak
                hanya pagi hari, malam sebelum tidur Bapak dan Ibu asrama juga melakukan doa bersama mahasiswa setelah
                sharing session. Dimana para mahasiswa diwajibkan bergantian memimpin ibadah,
                tujuannya adalah melatih keberanian mereka untuk dapat berbicara didepan umum.
                Salah satu ibu asrama mengatakan banyak suka dukanya dalam menghadapi setiap mahasiswa. Ada saja setiap
                harinya mahasiswa yang datang kepada mereka untuk bercerita baik itu tentang masalah keluarga,
                tentang masalah ekonomi, tentang bagaimana indahnya kehidupan pertemanan dikampus dan sebagainya. Bahkan
                Ibu Ria berkata sambil tersenyum ada mahasiswa yang mendatanginya tengah malam hanya karena mimpi buruk.
                Ya, karena mereka menganggap Bapak dan Ibu asrama mereka adalahh pengganti orangtua mereka selama di IT
                Del.
                Asrama di IT Del juga merupakan sarana pendidikan dalam pembentukan karakter mahasiswanya. Dimana para
                mahasiswa akan mendapatkan nilai prilaku setiap tiga bulan sekali dari setiap Bapak dan Ibu asramanya.
                Nilai ini akan sangat berpengaruh kepada mahasiswa apabila mereka ingin mengikuti suatu organisasi
                kemahasiswaan. Begitu juga untuk mendapatkan beasiswa, baik itu beasiswa ekonomi maupun beasiswa
                prestasi. Karena bukan hanya nilai yang baik yang diharapkan dari mahasiswanya tapi juga mahasiswa yang
                mempunyai akhlak yang baik.
                IT Del selalu berusaha menciptakan manusia yang tidak hanya pintar tapi juga yang MarTuhan, Maroha dan
                Marbisuk (Berketuhanan, Berperasaan dan Bijaksana).</h4>


